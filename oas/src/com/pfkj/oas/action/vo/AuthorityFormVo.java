public class AuthorityFormVo {
	private String id;
	private String name;
	private String selgnlb;
	private String desc;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSelgnlb() {
		return selgnlb;
	}
	public void setSelgnlb(String selgnlb) {
		this.selgnlb = selgnlb;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	
}
